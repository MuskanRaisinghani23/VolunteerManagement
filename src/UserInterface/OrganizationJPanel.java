    private void populateOrgEmployeesTbl() {
        Organization org = business.getOrganization();
        headinglbl.setText(org.getName());
        
//        Table populate
        DefaultTableModel dtm = (DefaultTableModel) updateEmpTbl.getModel();
        dtm.setRowCount(0);
        for(Employee e: org.getEmpDirectory()){
            Object[] row = new Object[4];
            row[0] = e.getPersonid();
            row[1] = e.getName();
            row[2] = e.getRole();
            row[3] = e.getPhone();
            dtm.addRow(row);      
        }
    }

    private void populateVolunteerTbl() {
        Organization org= business.getOrganization();
        ArrayList<Volunteer> vollist = business.getVolunteerdirectory().getAllVol();    
        ArrayList<Volunteer> selectedvolunteer = new ArrayList<>();
        for(Volunteer v : vollist){
            String volcity = v.getCity();
            String volcountry = v.getCountry();
            if(volcity.equalsIgnoreCase(org.getCity()) && volcountry.equalsIgnoreCase(org.getCountry())){
                selectedvolunteer.add(v);
            }
        }
                
        DefaultTableModel dtm = (DefaultTableModel) volunteerTbl.getModel();
        dtm.setRowCount(0);
        for(Volunteer v : selectedvolunteer){
            Object[] row = new Object[7];
            row[0] = v.getPersonid();
            row[1] = v.getName();
            
            row[3] = v.getCity();
            row[4] = v.getAge();
            row[5] = v.getGender();
            OrganizationVolunteer ov = business.getOrganizationvolunteerdirectory().findRequestIfPresent(org, v);
            if(ov!=null){
                row[6] = ov.getRequeststatus();
//                Showcase volunteer contact only when request is accpted
                if(ov.getRequeststatus().equalsIgnoreCase("Accepted")){
                    row[2] = v.getPhone();
                }
                else{
                    row[2] = "";
                }
            }
            else{
                row[6] = "";
            }
            dtm.addRow(row);      
        }
        
    }

